
export function shorten(string, more = false) {
    if(typeof string !== 'string') return ""
    if (more) return string.replace(new RegExp("[、;].+$", ''), '')
    else return string.replace(new RegExp("([^、;]*[、;][^、;]*).*", 'g'), '$1')
}

